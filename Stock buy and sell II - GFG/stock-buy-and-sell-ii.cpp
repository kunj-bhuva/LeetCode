class Solution {
  public:
    int stockBuyAndSell(int n, vector<int> &prices) {
     int ans=0;
        int buy=prices[0];
        for(int i=0;i<n-1;i++)
        {
            buy=prices[i];
            int sell=prices[i+1];
            if(buy<sell)
            {
                ans+=sell-buy;
            }
        }
        // if(ans.empty())
        // cout<<"0";
        // else
        // cout<<"1";
        return ans;
        // code here
    }
};


